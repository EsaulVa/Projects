% debug_inverse_trace.m
% Отладочный клиент: строит ТСН (синяя линия) и отмечает точки расходимости
clear; clc; close all;

% ======================================================================
% 1. Загрузка данных
% ======================================================================
if ~exist('LU_data.mat', 'file'), error('LU_data.mat не найден'); end
if ~exist('R_points.mat', 'file'), error('R_points.mat не найден'); end

data_lu = load('LU_data.mat');
r_etalon = data_lu.r;
if size(r_etalon,1) ~= 3, r_etalon = r_etalon'; end
fprintf('Эталонная ЛУ: %d точек\n', size(r_etalon,2));

data_r = load('R_points.mat');
R_valid = data_r.R_valid;          % глобальная система (3×M)
s_valid = data_r.s_valid;
fprintf('ТСН: %d точек\n', size(R_valid,2));

% ======================================================================
% 2. Поверхности и z_offset
% ======================================================================
phi_c_opravka = [0.0000000005642, -0.0000003012748, 0.0000605882383, -0.0099656628535, 2.9503573330764];
R_c_opravka   = [-344.1468891010463, 3932.5139101580062, -17756.7012553763525, ...
                 39582.6812110246392, -43518.6731429065403, 19122.1758646943599];
bound_opravka = [0, 234.27, 534.27, 768.54];
cyl_r_opravka = 251.705;
E2 = RevolutionSurface(phi_c_opravka, R_c_opravka, bound_opravka, cyl_r_opravka);

phi_c_safe = [0.0000000000176, -0.0000000319663, 0.0000178315076, -0.0066486075257, 2.9473869159379];
R_c_safe   = [-200.4096721343111, 2428.8709925850990, -11585.7546890810463, ...
              27152.4105364360366, -31195.5446114188999, 14397.6607910855146];
bound_safe = [0, 327.978, 627.978, 955.956];
cyl_r_safe = 352.387;
z_offset = (bound_safe(4) - bound_opravka(4)) / 2;   % = 93.708 мм
fprintf('z_offset = %.3f мм\n', z_offset);

% ======================================================================
% 3. Приведение R(z) к локальной системе оправки
% ======================================================================
R_valid_local = R_valid;
R_valid_local(3,:) = R_valid_local(3,:) - z_offset;
R_traj = Trajectory(R_valid_local);
z_max = R_traj.totalLength();
fprintf('Длина R(z) в локальной системе: %.2f мм\n', z_max);

% ======================================================================
% 4. Начальные условия (первая точка эталонной ЛУ)
% ======================================================================
lu_traj_ref = Trajectory(r_etalon);
r0 = lu_traj_ref.getPoint(1);
[u0, v0] = E2.uv_from_point(r0');
tau0 = lu_traj_ref.getTangent(1);
[ru0, rv0] = E2.derivatives(u0, v0);
E0 = dot(ru0, ru0); F0 = dot(ru0, rv0); G0 = dot(rv0, rv0);
M = [E0, F0; F0, G0];
rhs = [dot(tau0, ru0); dot(tau0, rv0)];
uv = M \ rhs;
du0_s = uv(1); dv0_s = uv(2);
fprintf('Начальная точка: u0=%.4f, v0=%.4f\n', u0, v0);

% ======================================================================
% 5. Параметры сходимости (щадящие)
% ======================================================================
DeltaZ = 1.0;          % мм
Percentage = 90;       % % (k ≈ 0.356)
Nsteps = 20000;

% ======================================================================
% 6. Запуск восстановления с записью диагностики
% ======================================================================
[u_rec, v_rec, s_rec, z_rec, dist_arr, bad_idx] = recoverLayer_rev_debug(...
    E2, R_traj, [0, z_max], u0, v0, du0_s, dv0_s, DeltaZ, Percentage, Nsteps);

% ======================================================================
% 7. Визуализация ТСН (синяя линия) и точек расходимости (красные маркеры)
% ======================================================================
figure('Name','ТСН и точки расходимости','Color','w');
% Синяя линия – вся траектория R(z) в глобальной системе (исходная)
plot3(R_valid(1,:), R_valid(2,:), R_valid(3,:), 'b-', 'LineWidth', 1.5); hold on;
% Красные маркеры – точки, где началась расходимость (по индексам из bad_idx)
if ~isempty(bad_idx)
    % Нужно сопоставить индексы bad_idx с точками R_valid
    % bad_idx – это номера шагов интегрирования (1..Nsteps+1)
    % Поскольку z_rec и s_rec синхронизированы, ищем ближайший z в исходном R_valid
    z_bad = z_rec(bad_idx);
    for k = 1:length(z_bad)
        [~, idx] = min(abs(s_valid - z_bad(k)));
        plot3(R_valid(1,idx), R_valid(2,idx), R_valid(3,idx), 'ro', 'MarkerSize', 8, 'MarkerFaceColor', 'r');
    end
end
xlabel('X'); ylabel('Y'); zlabel('Z');
title('Траектория R(z) (синяя) и точки расходимости (красные)');
grid on; axis equal; view(3);
hold off;

% Дополнительные графики
figure('Name','Диагностика');
subplot(3,1,1);
plot(z_rec, dist_arr, 'b.-'); grid on;
xlabel('z (мм)'); ylabel('||R - r|| (мм)');
title('Расстояние от точки схода до текущей точки на оправке');
subplot(3,1,2);
plot(z_rec, s_rec, 'r.-'); grid on;
xlabel('z (мм)'); ylabel('s (мм)');
title('Восстановленная длина дуги линии укладки');
subplot(3,1,3);
dsdz = diff(s_rec)./diff(z_rec);
plot(z_rec(2:end), dsdz, 'g.-'); grid on;
xlabel('z (мм)'); ylabel('ds/dz');
title('Производная ds/dz');
sgtitle('Отладочная информация');