#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
rhs_calculator_annotated.py
============================
Аннотированная версия RightHandSideCalculator.

НАУЧНОЕ ОБОСНОВАНИЕ
-------------------
Этот модуль реализует математическую модель Савина А.Г.
(диссертация, глава 3.2, формулы (3.40)--(3.41))
для обратной задачи намотки: по известной траектории точки схода нити R(z)
восстанавливается линия укладки r(u(s), v(s)) на поверхности оправки.

КЛЮЧЕВЫЕ ФОРМУЛЫ САВИНА
------------------------
(3.24)  Направление касательной к ЛУ при ненулевой невязке δ:
        dr/ds = [ (R-r)/|R-r| - δ·m ] / sqrt(1-δ²)

(3.40)  Решение линейной системы для контравариантных компонентов u', v':
        u' = (G·b₁ - F·b₂) / (EG-F²)
        v' = (E·b₂ - F·b₁) / (EG-F²)
        где b₁ = <b, rᵤ>, b₂ = <b, rᵥ>

(3.41)  Отношение дифференциалов ds/dz (главная + корректирующая части):
        ds/dz = [ <R'z, m> / (|R-r|·II) ]·α  +  [ δ / II ]·α·k
        где II = L·u'² + 2M·u'·v' + N·v'² — вторая квадратичная форма
        α = 1/√(1-δ²),  k — коэффициент усиления коррекции

(3.36)  Переход от параметра s к параметру z:
        du/dz = u' · ds/dz
        dv/dz = v' · ds/dz

ОГРАНИЧЕНИЯ МОДЕЛИ САВИНА
--------------------------
1. Коррекция пропорциональна невязке δ (через k). Это ОДНОКРАТНЫЙ
   пропорциональный шаг, а не итерационный корректор до обнуления δ.
   Следствие: при больших шагах dz невязка накапливается (дрейф).

2. Интегрирование ведётся в 3D-пространстве через параметр s.
   Точка r(s) может получить нормальную компоненту ошибки,
   которая не компенсируется строго.

3. Для поверхности вращения (PiecewisePolynomialRevolution) F=0, M=0,
   поэтому формула Крамера упрощается до u'=b₁/E, v'=b₂/G.
   В общем случае (произвольная поверхность) F≠0 и формула (3.40)
   работает в полном виде.

СРАВНЕНИЕ С DAE-ПОДХОДОМ (наш отчёт)
------------------------------------
В отчёте предлагается:
• Условие контакта формализовать как алгебраическую связь Φ=0.
• Предиктор — аналитическое дифференцирование связи в координатах (u,v).
• Корректор — Ньютона с КВАДРАТИЧНОЙ сходимостью до Φ=0.
• Точка ВСЕГДА лежит на поверхности, дрейф исключён.

Этот модуль (rhs_calculator) использует КЛАССИЧЕСКИЙ подход Савина.
"""

import numpy as np
from typing import Optional
from geometry.tsurfaces import AnalyticalSurface
from core.trajectory import Trajectory


class RightHandSideCalculator:
    """
    Вычислитель правых частей системы (3.41) Савина.

    На каждом шаге интегрирования по параметру z (длина дуги ТСН)
    вычисляет производные du/dz, dv/dz криволинейных координат линии укладки.
    """

    def __init__(
        self,
        surface: AnalyticalSurface,
        trajectory: Trajectory,
        k: float = 1.0,
        max_ds_dz: Optional[float] = None,
        delta_clip: float = 0.999,
        eps: float = 1e-12
    ):
        """
        Параметры
        ---------
        surface : AnalyticalSurface
            Поверхность оправки. Должна предоставлять:
            • derivatives(u,v) -> {r, ru, rv, normal}
            • first_fundamental_form(u,v) -> (E, F, G)
            • second_fundamental_form(u,v) -> (L, M, N)
        trajectory : Trajectory
            Траектория точки схода нити R(z) с натуральной параметризацией.
        k : float, default 1.0
            Коэффициент усиления КОРРЕКТИРУЮЩЕГО члена в ds/dz.
            Соответствует k в формуле (3.41) Савина.
            k=0 — нет коррекции (чистое интегрирование).
            k=1 — стандартная коррекция Савина.
            k>1 — агрессивная коррекция (может привести к неустойчивости).
        max_ds_dz : float, optional
            Максимальное |ds/dz|. Ограничивает скорость намотки
            при резких изменениях геометрии.
        delta_clip : float, default 0.999
            Максимальное |δ| для вычисления α=1/√(1-δ²).
            При |δ|→1 знаменатель обращается в ноль — критическая ситуация
            (нить почти параллельна поверхности).
        eps : float, default 1e-12
            Защита от деления на ноль.
        """
        self.surface = surface
        self.trajectory = trajectory
        self.k = k
        self.max_ds_dz = max_ds_dz
        self.delta_clip = delta_clip
        self.eps = eps

    def __call__(self, z: float, state: np.ndarray) -> tuple[float, float]:
        """
        Вычисляет правые части du/dz, dv/dz в точке z.

        Алгоритм (по шагам, с отсылками к формулам Савина):
        ---------------------------------------------------
        1. Получить R(z) и R'(z) — траектория точки схода.
        2. Получить геометрию поверхности в (u,v): r, rᵤ, rᵥ, m.
        3. Вычислить невязку δ = <(R-r)/|R-r|, m> — формула (3.18).
        4. Вычислить α = 1/√(1-δ²) — нормировка из (3.24).
        5. Построить вектор b = [(R-r)/|R-r| - δ·m]·α — формула (3.24).
        6. Решить систему для u', v' через формулу Крамера (3.40).
        7. Вычислить II = L·u'² + 2M·u'·v' + N·v'² — вторая форма.
        8. Вычислить ds/dz по формуле (3.41) с корректирующим членом.
        9. Перейти к du/dz, dv/dz через (3.36).

        Параметры
        ---------
        z : float
            Текущее значение параметра траектории (длина дуги ТСН).
        state : np.ndarray, shape (2,)
            [u, v] — криволинейные координаты на поверхности.

        Возвращает
        ----------
        du_dz : float
        dv_dz : float
        """
        u, v = state

        # === ШАГ 1. Траектория точки схода ===
        # R(z) — радиус-вектор точки схода нити (внешняя поверхность безопасности).
        # R'(z) — единичный касательный вектор к траектории ТСН.
        R = self.trajectory.R(z)
        R_deriv = self.trajectory.R_deriv(z)

        # === ШАГ 2. Геометрия поверхности оправки в точке (u,v) ===
        # r(u,v) — радиус-вектор точки на оправке.
        # rᵤ, rᵥ — касательные векторы к координатным линиям.
        # m — единичная внешняя нормаль к поверхности.
        geom = self.surface.derivatives(u, v)
        r = geom['r']
        ru = geom['ru']   # ∂r/∂u
        rv = geom['rv']   # ∂r/∂v
        m = geom['normal']  # единичная нормаль

        # === ШАГ 3. Вектор нити и её длина ===
        # diff = R - r — вектор свободного участка нити.
        # diff_norm = λ = |R-r| — длина свободного участка (формула 2.1 Савина).
        diff = R - r
        diff_norm = np.linalg.norm(diff)
        if diff_norm < self.eps:
            # Вырожденный случай: точка схода совпадает с оправкой.
            # На практике невозможен, но возвращаем нули для устойчивости.
            return 0.0, 0.0

        # === ШАГ 4. Невязка δ (формула 3.18 Савина) ===
        # δ = < (R-r)/|R-r|, m >
        # Идеально: δ = 0 (нить лежит в касательной плоскости).
        # Реально: |δ| > 0 из-за численных ошибок интегрирования.
        diff_unit = diff / diff_norm
        delta = np.dot(diff_unit, m)

        # Защита: если |δ| > 0.999, то α = 1/√(1-δ²) взрывается.
        # Это означает, что нить почти параллельна нормали — критическая зона.
        delta = np.clip(delta, -self.delta_clip, self.delta_clip)

        # === ШАГ 5. Коэффициент α (из формулы 3.24) ===
        # α = 1 / √(1 - δ²)
        # При δ = 0 (идеальный контакт) α = 1.
        # При |δ| → 1 α → ∞ — нить "встала" перпендикулярно поверхности.
        alpha = 1.0 / np.sqrt(max(self.eps, 1.0 - delta * delta))

        # === ШАГ 6. Вектор b — скорректированная касательная (3.24) ===
        # b = [ (R-r)/|R-r| - δ·m ] · α
        #
        # ПРИМЕЧАНИЕ: при δ = 0 это просто единичный вектор нити.
        # При δ ≠ 0 это проекция направления нити на касательную плоскость
        # с компенсацией нормальной составляющей δ.
        b = (diff_unit - delta * m) * alpha

        # === ШАГ 7. Компоненты b в касательном базисе ===
        # b₁ = <b, rᵤ>, b₂ = <b, rᵥ>
        # Это ковариантные компоненты вектора b.
        b1 = np.dot(b, ru)
        b2 = np.dot(b, rv)

        # === ШАГ 8. Первая квадратичная форма (I форма) ===
        # E = <rᵤ, rᵤ>, F = <rᵤ, rᵥ>, G = <rᵥ, rᵥ>
        # det = EG - F² — определитель метрического тензора.
        #
        # Для поверхности вращения F = 0 (координатные линии ортогональны),
        # поэтому det = E·G.
        # Для общей поверхности F ≠ 0 и требуется полная формула Крамера.
        E, F, G = self.surface.first_fundamental_form(u, v)
        det = E * G - F * F
        if abs(det) < self.eps:
            raise ValueError(
                f"Вырожденная метрика в точке u={u:.4f}, v={v:.4f}: "
                f"det={det:.2e}. Поверхность вырождена или точка на оси."
            )

        # === ШАГ 9. Решение для u', v' через формулу Крамера (3.40) ===
        # Система:
        #   [E  F] [u'] = [b₁]
        #   [F  G] [v']   [b₂]
        #
        # Решение:
        #   u' = (G·b₁ - F·b₂) / det
        #   v' = (E·b₂ - F·b₁) / det
        #
        # Для поверхности вращения (F=0) упрощается до:
        #   u' = b₁ / E,  v' = b₂ / G.
        u_prime = (G * b1 - F * b2) / det
        v_prime = (-F * b1 + E * b2) / det

        # === ШАГ 10. Вторая квадратичная форма (II форма) ===
        # L, M, N — коэффициенты II формы.
        # II = L·u'² + 2M·u'·v' + N·v'² — нормальная кривизна поверхности
        # в направлении вектора (u', v').
        #
        # Для поверхности вращения M = 0.
        # Для цилиндра L = 0 (r'' = 0), N = -r/√(1+r'²).
        L, M, N_coef = self.surface.second_fundamental_form(u, v)
        II = L * u_prime**2 + 2.0 * M * u_prime * v_prime + N_coef * v_prime**2

        # Защита от деления на ноль.
        # II → 0 означает, что направление нити асимптотическое
        # (кривизна поверхности в этом направлении нулевая).
        # Это фундаментальное ограничение метода (см. отчёт, п. 6.2).
        if abs(II) < self.eps:
            II = np.copysign(self.eps, II) if II != 0 else self.eps

        # === ШАГ 11. Скалярное произведение <R'(z), m> ===
        # Это числитель главной части ds/dz (формула 3.41).
        # Показывает, насколько быстро точка схода движется вдоль нормали
        # к поверхности оправки.
        R_deriv_dot_n = np.dot(R_deriv, m)

        # === ШАГ 12. Вычисление ds/dz по формуле (3.41) Савина ===
        #
        # Формула (3.41):
        #   ds/dz = [ <R'z, m> / (|R-r| · II) ] · α  +  [ δ / II ] · α · k
        #
        # ГЛАВНАЯ ЧАСТЬ (main_term):
        #   Описывает геометрическое соотношение между движением ТСН
        #   и движением по ЛУ. Без коррекции (k=0) это чистое
        #   интегрирование уравнения сохранения связи.
        #
        # КОРРЕКТИРУЮЩАЯ ЧАСТЬ (correction_term):
        #   δ / II — это пропорциональная коррекция натурального параметра s.
        #   Умножается на α (нормировка) и k (усиление).
        #
        # КРИТИЧЕСКИЙ КОММЕНТАРИЙ:
        #   Это ПРОПОРЦИОНАЛЬНАЯ коррекция, а не ИТЕРАЦИОННАЯ.
        #   Она уменьшает δ, но НЕ ОБНУЛЯЕТ его.
        #   При больших шагах dz невязка накапливается (дрейф).
        #   В отчёте предлагается заменить это на корректор Ньютона,
        #   который даёт квадратичную сходимость к Φ=0.
        main_term = (R_deriv_dot_n / (diff_norm * II)) * alpha
        correction_term = (delta / II) * alpha * self.k
        ds_dz = main_term + correction_term

        # === ШАГ 13. Ограничение скорости намотки ===
        # Если |ds/dz| слишком велико, ограничиваем его.
        # Это защита от численного взрыва на участках с резкой кривизной.
        if self.max_ds_dz is not None:
            if abs(ds_dz) > self.max_ds_dz:
                ds_dz = np.copysign(self.max_ds_dz, ds_dz)

        # === ШАГ 14. Переход к du/dz, dv/dz (формула 3.36) ===
        # du/dz = u' · ds/dz
        # dv/dz = v' · ds/dz
        #
        # Здесь происходит переход от натурального параметра ЛУ (s)
        # к параметру траектории ТСН (z).
        du_dz = u_prime * ds_dz
        dv_dz = v_prime * ds_dz

        return du_dz, dv_dz
