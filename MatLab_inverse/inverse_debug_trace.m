% inverse_debug_trace.m
% Отладочная версия: восстановление линии укладки с сохранением диагностики
% и визуализацией точек расходимости на траектории R(z).

clear; clc; close all;

% ======================================================================
% 1. Загрузка данных
% ======================================================================
if ~exist('LU_data.mat', 'file'), error('LU_data.mat не найден'); end
if ~exist('R_points.mat', 'file'), error('R_points.mat не найден'); end

data_lu = load('LU_data.mat');
r_etalon = data_lu.r;
if size(r_etalon,1) ~= 3, r_etalon = r_etalon'; end
fprintf('Эталонная ЛУ: %d точек\n', size(r_etalon,2));

data_r = load('R_points.mat');
R_valid = data_r.R_valid;      % глобальная система
fprintf('R(z) исходная: %d точек\n', size(R_valid,2));

% ======================================================================
% 2. Поверхности и z_offset
% ======================================================================
phi_c_opravka = [0.0000000005642, -0.0000003012748, 0.0000605882383, -0.0099656628535, 2.9503573330764];
R_c_opravka   = [-344.1468891010463, 3932.5139101580062, -17756.7012553763525, ...
                 39582.6812110246392, -43518.6731429065403, 19122.1758646943599];
bound_opravka = [0, 234.27, 534.27, 768.54];
cyl_r_opravka = 251.705;
E2 = RevolutionSurface(phi_c_opravka, R_c_opravka, bound_opravka, cyl_r_opravka);

phi_c_safe = [0.0000000000176, -0.0000000319663, 0.0000178315076, -0.0066486075257, 2.9473869159379];
R_c_safe   = [-200.4096721343111, 2428.8709925850990, -11585.7546890810463, ...
              27152.4105364360366, -31195.5446114188999, 14397.6607910855146];
bound_safe = [0, 327.978, 627.978, 955.956];
cyl_r_safe = 352.387;
z_offset = (bound_safe(4) - bound_opravka(4)) / 2;   % 93.708
fprintf('z_offset = %.3f мм\n', z_offset);

% ======================================================================
% 3. Приведение R(z) к локальной системе оправки
% ======================================================================
R_valid_local = R_valid;
R_valid_local(3,:) = R_valid_local(3,:) - z_offset;
% Обрезаем по Z оправки
mask = (R_valid_local(3,:) >= E2.u_min) & (R_valid_local(3,:) <= E2.u_max);
R_valid_local = R_valid_local(:, mask);
fprintf('После обрезки по Z оправки: %d точек\n', size(R_valid_local,2));

R_traj = Trajectory(R_valid_local);
z_max = R_traj.totalLength();
fprintf('Длина R(z): %.2f мм\n', z_max);

% ======================================================================
% 4. Начальные условия (из эталонной ЛУ)
% ======================================================================
lu_traj_ref = Trajectory(r_etalon);
r0 = lu_traj_ref.getPoint(100);
[u0, v0] = E2.uv_from_point(r0');
tau0 = lu_traj_ref.getTangent(100);
[ru0, rv0] = E2.derivatives(u0, v0);
E0 = dot(ru0, ru0); F0 = dot(ru0, rv0); G0 = dot(rv0, rv0);
M = [E0, F0; F0, G0];
rhs = [dot(tau0, ru0); dot(tau0, rv0)];
uv = M \ rhs;
du0_s = uv(1); dv0_s = uv(2);
fprintf('Начальная точка: u0=%.4f, v0=%.4f, du/ds=%.6f, dv/ds=%.6f\n', u0, v0, du0_s, dv0_s);

% ======================================================================
% 5. Параметры интегрирования
% ======================================================================
DeltaZ = 0.5;
Percentage = 50;
Nsteps = 2000;

% ======================================================================
% 6. Восстановление с сохранением истории
% ======================================================================
[u_rec, v_rec, s_rec, z_rec, dist_list, bad_idx] = recoverLayer_debug(E2, R_traj, [0, z_max], ...
    u0, v0, du0_s, dv0_s, DeltaZ, Percentage, Nsteps);

% ======================================================================
% 7. Визуализация R(z) и точек расходимости
% ======================================================================
% Точки R(z) в локальной системе (уже есть R_valid_local)
% Визуализируем всю траекторию R(z)
figure('Name','Траектория R(z) с маркерами расходимости','Color','w');
plot3(R_valid_local(1,:), R_valid_local(2,:), R_valid_local(3,:), 'b-', 'LineWidth', 1);
hold on;
% Маркеры плохих точек (по индексам из bad_idx)
if ~isempty(bad_idx)
    for k = 1:length(bad_idx)
        idx = bad_idx(k);
        if idx <= size(R_valid_local,2)
            plot3(R_valid_local(1,idx), R_valid_local(2,idx), R_valid_local(3,idx), ...
                'ro', 'MarkerSize', 10, 'MarkerFaceColor', 'r');
        end
    end
    legend('R(z)', 'Точка начала расходимости');
else
    legend('R(z)');
end
xlabel('X'); ylabel('Y'); zlabel('Z');
title('Траектория точки схода R(z) – отмечены места, где начинается бред');
axis equal; grid on; view(3);

% ======================================================================
% 8. График dist и s от z_param
% ======================================================================
figure('Name','Диагностика расходимости');
subplot(2,1,1);
plot(z_rec, dist_list, 'b.-'); grid on;
xlabel('z (параметр траектории, мм)'); ylabel('||R - r||, мм');
title('Расстояние от точки схода до восстановленной точки на оправке');
subplot(2,1,2);
plot(z_rec, s_rec, 'r.-'); grid on;
xlabel('z (мм)'); ylabel('s (восстановленная длина дуги, мм)');
title('Восстановленный натуральный параметр s(z)');

% ======================================================================
% 9. Сохранение результатов
% ======================================================================
save('inverse_debug.mat', 'z_rec', 's_rec', 'dist_list', 'bad_idx');
fprintf('Отладочные данные сохранены в inverse_debug.mat\n');